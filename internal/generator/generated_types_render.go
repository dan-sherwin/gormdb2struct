func generatedValueOf(value any) (driver.Value, error) {
	if value == nil {
		return nil, nil
	}
	if valuer, ok := value.(driver.Valuer); ok {
		return valuer.Value()
	}

	rv := reflect.ValueOf(value)
	if !rv.IsValid() {
		return nil, nil
	}

	switch {
	case rv.Type() == reflect.TypeOf(time.Time{}):
		return rv.Interface().(time.Time), nil
	case rv.Kind() == reflect.String:
		return rv.String(), nil
	case rv.Kind() == reflect.Bool:
		return rv.Bool(), nil
	case rv.Kind() >= reflect.Int && rv.Kind() <= reflect.Int64:
		return rv.Int(), nil
	case rv.Kind() >= reflect.Uint && rv.Kind() <= reflect.Uint64:
		const maxInt64 = ^uint64(0) >> 1
		if rv.Uint() > maxInt64 {
			return nil, fmt.Errorf("generated type value overflows int64: %d", rv.Uint())
		}
		return int64(rv.Uint()), nil
	case rv.Kind() == reflect.Float32 || rv.Kind() == reflect.Float64:
		return rv.Float(), nil
	case rv.Kind() == reflect.Slice && rv.Type().Elem().Kind() == reflect.Uint8:
		return append([]byte(nil), rv.Bytes()...), nil
	}

	return nil, fmt.Errorf("unsupported generated type value %T", value)
}

