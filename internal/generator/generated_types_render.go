type generatedDomainTemplateData struct {
	PackageName       string
	DBType            string
	GoType            string
	UnderlyingGoType  string
	Imports           []string
	ConstraintComment []string
	RegexPattern      string
	RegexVarName      string
}

func writeGeneratedTypesPackage(pkg generatedTypesPackage) error {
	if err := os.MkdirAll(pkg.OutputDir, 0o755); err != nil {
		return fmt.Errorf("create generated types output directory %s: %w", pkg.OutputDir, err)
	}

	if err := writeGeneratedTypesHelperFile(pkg); err != nil {
		return err
	}
	for _, enumType := range pkg.Enums {
		if err := writeGeneratedEnumFile(pkg, enumType); err != nil {
			return err
		}
	}
	for _, domainType := range pkg.Domains {
		if err := writeGeneratedDomainFile(pkg, domainType); err != nil {
			return err
		}
	}
	for _, arrayType := range pkg.Arrays {
		if err := writeGeneratedArrayFile(pkg, arrayType); err != nil {
			return err
		}
	}

	return nil
}

func writeGeneratedTypesHelperFile(pkg generatedTypesPackage) error {
	rendered, err := renderTemplate("generated_types_helpers", generatedTypesHelpersTemplate, struct {
		PackageName string
	}{
		PackageName: pkg.PackageName,
	})
	if err != nil {
		return err
	}

	outFile := filepath.Join(pkg.OutputDir, "zz_generated_helpers.gen.go")
	if err := writeFormattedGoFile(outFile, rendered); err != nil {
		return err
	}

	return nil
}

func writeGeneratedEnumFile(pkg generatedTypesPackage, enumType generatedEnumType) error {
	rendered, err := renderTemplate("generated_enum_type", generatedEnumTemplate, struct {
		PackageName string
		generatedEnumType
	}{
		PackageName:       pkg.PackageName,
		generatedEnumType: enumType,
	})
	if err != nil {
		return err
	}

	outFile := filepath.Join(pkg.OutputDir, enumType.FileName)
	if err := writeFormattedGoFile(outFile, rendered); err != nil {
		return err
	}

	return nil
}

func writeGeneratedDomainFile(pkg generatedTypesPackage, domainType generatedDomainType) error {
	imports := append([]string{
		"database/sql/driver",
		"fmt",
		"gorm.io/gorm",
		"gorm.io/gorm/schema",
	}, domainType.Imports...)
	if domainType.RegexPattern != "" {
		imports = append(imports, "regexp")
	}
	imports = uniqueStrings(imports)
	sort.Strings(imports)

	commentLines := make([]string, 0, len(domainType.Constraints))
	for _, constraint := range domainType.Constraints {
		commentLines = append(commentLines, "Source constraint: "+constraint)
	}

	rendered, err := renderTemplate("generated_domain_type", generatedDomainTemplate, generatedDomainTemplateData{
		PackageName:       pkg.PackageName,
		DBType:            domainType.DBType,
		GoType:            domainType.GoType,
		UnderlyingGoType:  domainType.UnderlyingGoType,
		Imports:           imports,
		ConstraintComment: commentLines,
		RegexPattern:      domainType.RegexPattern,
		RegexVarName:      strcase.ToLowerCamel(domainType.GoType) + "Pattern",
	})
	if err != nil {
		return err
	}

	outFile := filepath.Join(pkg.OutputDir, domainType.FileName)
	if err := writeFormattedGoFile(outFile, rendered); err != nil {
		return err
	}

	return nil
}

func writeGeneratedArrayFile(pkg generatedTypesPackage, arrayType generatedArrayType) error {
	rendered, err := renderTemplate("generated_array_type", generatedArrayTemplate, struct {
		PackageName string
		generatedArrayType
	}{
		PackageName:        pkg.PackageName,
		generatedArrayType: arrayType,
	})
	if err != nil {
		return err
	}

	outFile := filepath.Join(pkg.OutputDir, arrayType.FileName)
	if err := writeFormattedGoFile(outFile, rendered); err != nil {
		return err
	}

	return nil
}

func renderTemplate(name, tmpl string, data any) ([]byte, error) {
	parsed, err := template.New(name).Parse(tmpl)
	if err != nil {
		return nil, fmt.Errorf("parse %s template: %w", name, err)
	}

	var buf bytes.Buffer
	if err := parsed.Execute(&buf, data); err != nil {
		return nil, fmt.Errorf("render %s template: %w", name, err)
	}

	return buf.Bytes(), nil
}

func writeFormattedGoFile(outFile string, src []byte) error {
	formatted, err := format.Source(src)
	if err != nil {
		return fmt.Errorf("format generated Go file %s: %w", outFile, err)
	}
	if err := os.WriteFile(outFile, formatted, 0o644); err != nil {
		return fmt.Errorf("write generated Go file %s: %w", outFile, err)
	}
	return nil
}

