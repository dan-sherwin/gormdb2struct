func generatedToTime(value any) (time.Time, error) {
	switch v := value.(type) {
	case time.Time:
		return v, nil
	case string:
		return generatedParseTimeString(v)
	case []byte:
		return generatedParseTimeString(string(v))
	default:
		return time.Time{}, fmt.Errorf("cannot convert %T to time.Time", value)
	}
}

func generatedParseTimeString(value string) (time.Time, error) {
	layouts := []string{
		time.RFC3339Nano,
		time.RFC3339,
		"2006-01-02 15:04:05.999999999-07:00",
		"2006-01-02 15:04:05.999999999",
		"2006-01-02 15:04:05",
		"2006-01-02",
	}

	trimmed := strings.TrimSpace(value)
	for _, layout := range layouts {
		if parsed, err := time.Parse(layout, trimmed); err == nil {
			return parsed, nil
		}
	}
	return time.Time{}, fmt.Errorf("cannot parse %q as time.Time", value)
}

func generatedParsePostgresTextArray(src any) ([]string, error) {
	if src == nil {
		return nil, nil
	}

	var input string
	switch v := src.(type) {
	case string:
		input = v
	case []byte:
		input = string(v)
	default:
		return nil, fmt.Errorf("cannot scan %T into PostgreSQL text array", src)
	}

	if input == "" {
		return []string{}, nil
	}
	if len(input) < 2 || input[0] != '{' || input[len(input)-1] != '}' {
		return nil, fmt.Errorf("invalid PostgreSQL array literal %q", input)
	}

	input = input[1 : len(input)-1]
	if input == "" {
		return []string{}, nil
	}

	values := make([]string, 0, strings.Count(input, ",")+1)
	var current strings.Builder
	inQuotes := false
	escaped := false

	for i := 0; i < len(input); i++ {
		ch := input[i]
		if escaped {
			current.WriteByte(ch)
			escaped = false
			continue
		}

		if inQuotes {
			switch ch {
			case '\\':
				escaped = true
			case '"':
				inQuotes = false
			default:
				current.WriteByte(ch)
			}
			continue
		}

		switch ch {
		case '"':
			inQuotes = true
		case ',':
			values = append(values, current.String())
			current.Reset()
		default:
			current.WriteByte(ch)
		}
	}

	if escaped || inQuotes {
		return nil, fmt.Errorf("invalid PostgreSQL array literal %q", src)
	}

	values = append(values, current.String())
	return values, nil
}

func generatedFormatPostgresTextArray(values []string) string {
	if len(values) == 0 {
		return "{}"
	}

	encoded := make([]string, len(values))
	for i, value := range values {
		escaped := strings.ReplaceAll(value, "\\", "\\\\")
		escaped = strings.ReplaceAll(escaped, "\"", "\\\"")
		encoded[i] = "\"" + escaped + "\""
	}

	return "{" + strings.Join(encoded, ",") + "}"
}
